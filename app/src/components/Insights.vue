<script>
document.addEventListener('DOMContentLoaded', () => {
  const carousel = document.getElementById('carousel-track');
  const nextBtn = document.getElementById('next-btn');
  const prevBtn = document.getElementById('prev-btn');

  // âœ… Only run if carousel exists
  if (!carousel || !nextBtn || !prevBtn) return;

  const scrollAmount = carousel.offsetWidth * 0.9;

  nextBtn.addEventListener('click', () => {
    carousel.scrollBy({ left: scrollAmount, behavior: 'smooth' });
  });

  prevBtn.addEventListener('click', () => {
    carousel.scrollBy({ left: -scrollAmount, behavior: 'smooth' });
  });

  const updateButtons = () => {
    prevBtn.disabled = carousel.scrollLeft <= 0;
    nextBtn.disabled = carousel.scrollLeft + carousel.offsetWidth >= carousel.scrollWidth;
  };

  carousel.addEventListener('scroll', updateButtons);
  updateButtons();
});
</script>

